// ════════════════════════════ ATTENDANCE TAB ════════════════════════════
function todayISO() {
  const d = new Date();
  return d.toISOString().slice(0, 10);
}
function attKey(cls, date) { return `${cls}|${date}`; }

function initAttendance() {
  const dateInput = $('#attDate');
  if (!dateInput.value) dateInput.value = todayISO();
  renderAttendance();
}

function renderAttendance() {
  const cls  = $('#attClass').value;
  const date = $('#attDate').value;
  const key  = attKey(cls, date);
  const stateMap = attendanceState[key] || {};

  const list = students.filter(s => s.cls === cls);
  $('#attDateLbl').textContent = `${cls} · ${new Date(date).toDateString()} · ${list.length} students`;

  if (!list.length) {
    $('#attRows').innerHTML = `<div class="panel p-10 text-center text-gray-500 text-sm">No students enrolled in ${cls}.</div>`;
    updateAttCounts();
    return;
  }

  $('#attRows').innerHTML = list.map(s => {
    const status = stateMap[s.name];
    return `
      <div class="att-row">
        <div class="flex items-center gap-3 min-w-0">
          <span class="avatar" style="background:${colorFor(s.name)}">${initials(s.name)}</span>
          <div class="min-w-0">
            <div class="text-white text-sm truncate">${s.name}</div>
            <div class="text-xs text-gray-500 truncate">${s.guardian}</div>
          </div>
        </div>
        <div class="att-toggle" data-student="${s.name}">
          <button class="att-btn ${status==='present'?'is-present':''}" data-status="present">Present</button>
          <button class="att-btn ${status==='late'?'is-late':''}" data-status="late">Late</button>
          <button class="att-btn ${status==='absent'?'is-absent':''}" data-status="absent">Absent</button>
        </div>
      </div>
    `;
  }).join('');

  updateAttCounts();
}

function updateAttCounts() {
  const cls = $('#attClass').value;
  const date = $('#attDate').value;
  const map = attendanceState[attKey(cls, date)] || {};
  let p=0, l=0, a=0;
  Object.values(map).forEach(v => { if (v==='present') p++; else if (v==='late') l++; else if (v==='absent') a++; });
  $('#cntPresent').textContent = p;
  $('#cntLate').textContent = l;
  $('#cntAbsent').textContent = a;
}

document.addEventListener('click', (e) => {
  const btn = e.target.closest('.att-btn');
  if (!btn) return;
  const wrap = btn.parentElement;
  const studentName = wrap.dataset.student;
  const status = btn.dataset.status;
  const cls  = $('#attClass').value;
  const date = $('#attDate').value;
  const key = attKey(cls, date);
  attendanceState[key] = attendanceState[key] || {};
  attendanceState[key][studentName] = status;
  // update buttons in this row
  wrap.querySelectorAll('.att-btn').forEach(b => {
    b.classList.remove('is-present','is-late','is-absent');
    if (b.dataset.status === status) b.classList.add(`is-${status}`);
  });
  updateAttCounts();
});

