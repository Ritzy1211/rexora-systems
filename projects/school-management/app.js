$('#saveAttendance')?.addEventListener('click', () => {
  const cls  = $('#attClass').value;
  const date = $('#attDate').value;
  const map = attendanceState[attKey(cls, date)] || {};
  const count = Object.keys(map).length;
  const total = students.filter(s => s.cls === cls).length;
  if (count === 0) { toast('Mark at least one student first', 'brandPurple'); return; }
  toast(`Attendance saved · ${count}/${total} students recorded`);
  // update the dashboard chart number subtly
  const present = Object.values(map).filter(v => v === 'present').length;
  if (total) {
    const pct = Math.round((present / total) * 100);
    $('[data-kpi="attendance"]').textContent = pct + '%';
  }
});

