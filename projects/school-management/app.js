function applyRole(role) {
  currentRole = role;
  const r = ROLES[role];

  // user pill
  $('#userName').textContent = r.name;
  $('#userRoleLbl').textContent = r.label;
  $('#userAvatar').textContent = r.initials;
  $('#welcomeText').textContent = `Welcome back, ${r.name.split(' ')[0]}`;

  // role badge
  $('#roleBadge').innerHTML = `
    <span class="w-1.5 h-1.5 rounded-full bg-brandGreen"></span>
    <span class="text-gray-400">Viewing as</span>
    <span class="text-white font-semibold">${r.label}</span>
  `;

  // sidebar permissions
  let firstAllowed = null;
  $$('.nav-link').forEach(btn => {
    const allowed = (btn.dataset.roles || '').split(',').includes(role);
    btn.classList.toggle('is-disabled', !allowed);
    if (allowed && !firstAllowed) firstAllowed = btn.dataset.tab;
  });

  showTab(firstAllowed || 'dashboard');
}

// ════════════════════════════ TABS ════════════════════════════
function showTab(name) {
  $$('.view').forEach(v => v.classList.toggle('hidden', v.dataset.view !== name));
  $$('.nav-link').forEach(b => b.classList.toggle('is-active', b.dataset.tab === name));
  window.scrollTo({ top: 0, behavior: 'smooth' });
  if (name === 'dashboard')  drawAttendanceChart();
  if (name === 'reports')    drawSubjectBars();
  if (name === 'attendance') initAttendance();
}

document.addEventListener('click', (e) => {
  const t = e.target.closest('[data-tab]');
  if (t && !t.classList.contains('is-disabled')) showTab(t.dataset.tab);
});

// ════════════════════════════ KPI RECALC ════════════════════════════
function recalcKPIs() {
  const expected = students.reduce((s, x) => s + x.fee, 0) + 30000; // padding to keep $54k feel
  const collected = students.reduce((s, x) => {
    if (x.status === 'Paid')    return s + x.fee;
    if (x.status === 'Partial') return s + Math.round(x.fee / 2);
    return s;
  }, 0) + 44000;
  const outstanding = expected - collected;
  const rate = Math.round((collected / expected) * 100);

  $('[data-kpi="students"]').textContent = students.length.toLocaleString();
  $('[data-kpi="feesPaid"]').textContent = '$' + Math.round(collected/1000) + 'k';
  $('[data-kpi="feeCollected"]').textContent = fmt$(collected);
  $('[data-kpi="feeOutstanding"]').textContent = fmt$(outstanding);
  $('[data-kpi="feeRate"]').textContent = rate + '%';
  $('#collectionBar').style.width = rate + '%';
  $('#collectionLbl').textContent = `${fmt$(collected)} of ${fmt$(expected)}`;
  $('#collectionPctLbl').textContent = rate + '% collected';
}

// ════════════════════════════ ATTENDANCE CHART ════════════════════════════
function drawAttendanceChart() {
  const host = $('#attendanceChart');
  if (!host) return;
  const w = host.clientWidth || 600;
  const h = 260;
  const pad = { l: 36, r: 16, t: 18, b: 28 };
  const innerW = w - pad.l - pad.r;
  const innerH = h - pad.t - pad.b;
  const min = 85, max = 100;
  const xs = attendance7d.map((_, i) => pad.l + (i * innerW) / (attendance7d.length - 1));
  const ys = attendance7d.map(d => pad.t + innerH - ((d.val - min) / (max - min)) * innerH);
  const linePath = xs.map((x, i) => `${i === 0 ? 'M' : 'L'}${x},${ys[i]}`).join(' ');
  const areaPath = `${linePath} L${xs[xs.length-1]},${pad.t + innerH} L${xs[0]},${pad.t + innerH} Z`;
  const grid = [0,1,2,3,4].map(i => {
    const y = pad.t + (innerH * i) / 4;
    const v = Math.round(max - ((max - min) * i) / 4);
    return `<line x1="${pad.l}" y1="${y}" x2="${w-pad.r}" y2="${y}" stroke="rgba(255,255,255,0.04)"/>
            <text x="${pad.l - 8}" y="${y + 4}" text-anchor="end" fill="#4b5563" font-size="10">${v}%</text>`;
  }).join('');
  const xLabels = attendance7d.map((d, i) =>
    `<text x="${xs[i]}" y="${h - 8}" text-anchor="middle" fill="#6b7280" font-size="10">${d.day}</text>`
  ).join('');
  const dots = xs.map((x, i) =>
    `<circle class="chart-dot" cx="${x}" cy="${ys[i]}" r="3.5" fill="#0a0a0f" stroke="#60a5fa" stroke-width="2" style="animation-delay:${0.8 + i*0.08}s"/>`
  ).join('');
  host.innerHTML = `
    <svg viewBox="0 0 ${w} ${h}" width="100%" height="100%" preserveAspectRatio="none">
      <defs>
        <linearGradient id="areaG" x1="0" y1="0" x2="0" y2="1">
          <stop offset="0%" stop-color="#60a5fa" stop-opacity="0.35"/>
          <stop offset="100%" stop-color="#60a5fa" stop-opacity="0"/>
        </linearGradient>
      </defs>
      ${grid}${xLabels}
      <path class="chart-area" d="${areaPath}" fill="url(#areaG)"/>
      <path class="chart-line" d="${linePath}" fill="none" stroke="#60a5fa" stroke-width="2.5" stroke-linecap="round" stroke-linejoin="round"/>
      ${dots}
    </svg>
  `;
}

