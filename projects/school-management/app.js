// ════════════════════════════ REPORT GENERATION ════════════════════════════
const REPORT_STEPS = [
  { label: 'Compiling grades from teachers', dur: 1100 },
  { label: 'Calculating GPAs and rankings',  dur: 900  },
  { label: 'Building branded PDF report cards', dur: 1300 },
  { label: 'Emailing 1,284 parents',         dur: 1500 },
];

function openReportGen() {
  $('#reportSteps').innerHTML = REPORT_STEPS.map((s, i) => `
    <div class="report-step" data-step="${i}">
      <div class="report-step__icon" data-icon="${i}">${i+1}</div>
      <div class="text-gray-300">${s.label}</div>
    </div>
  `).join('');
  $('#reportBar').style.width = '0%';
  $('#reportPct').textContent = '0%';
  $('#reportStatus').textContent = 'Preparing…';
  $('#reportDone').classList.add('hidden');
  $('#reportModal').classList.add('show');
  runReportSteps();
}
function closeReportGen() { $('#reportModal').classList.remove('show'); }

function runReportSteps() {
  const total = REPORT_STEPS.reduce((s, x) => s + x.dur, 0);
  let elapsed = 0;
  const startStep = (i) => {
    if (i >= REPORT_STEPS.length) {
      $('#reportBar').style.width = '100%';
      $('#reportPct').textContent = '100%';
      $('#reportStatus').textContent = '✓ All reports delivered';
      // Add a fresh entry to recent reports
      reportFiles.unshift({
        type: 'PDF', color: 'brandPurple',
        title: 'Term 2 Report Cards — All Classes',
        meta: 'Auto-emailed to all guardians · just now',
      });
      renderReportList();
      $('#reportDone').classList.remove('hidden');
      toast('Reports sent to 1,284 parents');
      return;
    }
    const stepEl = $(`.report-step[data-step="${i}"]`);
    const iconEl = $(`.report-step__icon[data-icon="${i}"]`);
    if (i > 0) {
      const prev = $(`.report-step[data-step="${i-1}"]`);
      const prevIcon = $(`.report-step__icon[data-icon="${i-1}"]`);
      prev.classList.remove('is-active');
      prev.classList.add('is-done');
      prevIcon.innerHTML = '✓';
    }
    stepEl.classList.add('is-active');
    iconEl.innerHTML = `<div class="spinner" style="width:14px;height:14px;border-width:2px"></div>`;
    $('#reportStatus').textContent = REPORT_STEPS[i].label + '…';
    // animate bar over duration
    const startElapsed = elapsed;
    const startTime = performance.now();
    const tick = () => {
      const t = performance.now() - startTime;
      const localPct = Math.min(t / REPORT_STEPS[i].dur, 1);
      const cur = startElapsed + localPct * REPORT_STEPS[i].dur;
      const pct = Math.round((cur / total) * 100);
      $('#reportBar').style.width = pct + '%';
      $('#reportPct').textContent = pct + '%';
      if (localPct < 1) requestAnimationFrame(tick);
      else {
        elapsed += REPORT_STEPS[i].dur;
        startStep(i + 1);
      }
    };
    requestAnimationFrame(tick);
  };
  startStep(0);
}

document.addEventListener('click', (e) => {
  if (e.target.closest('[data-close-report]') || e.target.id === 'reportModal') closeReportGen();
});

